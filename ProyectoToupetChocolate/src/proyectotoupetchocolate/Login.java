    private void jButton_CONFIRMAR1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CONFIRMAR1ActionPerformed
        // TODO add your handling code here:
        int indice = jList_Eventos.getSelectedIndex();
        if (jTextField_edicionEmpleador.getText().isEmpty() || jTextField_edicionDuracion.getText().isEmpty()
                || jTextField_edicionPersonas.getText().isEmpty() || jTextField_edicionPaga.getText().isEmpty()
                || jTextField_edicionDireccion.getText().isEmpty() || jTextField_edicionFecha.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Debe completar todos los campos");

        } else {
            if (isNumeric(jTextField_edicionEmpleador.getText()) == false && isNumeric(jTextField_edicionDuracion.getText()) == true
                    && isNumeric(jTextField_edicionPersonas.getText()) == true && isDouble(jTextField_edicionPaga.getText()) == true) {
                eve.obtenerNodo(indice).getDato().setDireccion(jTextField_edicionDireccion.getText());
                eve.obtenerNodo(indice).getDato().setDuracionMinutos(Integer.parseInt(jTextField_edicionDuracion.getText()));
                eve.obtenerNodo(indice).getDato().setEmpleador(jTextField_edicionEmpleador.getText());
                eve.obtenerNodo(indice).getDato().setFecha(jTextField_edicionFecha.getText());
                eve.obtenerNodo(indice).getDato().setPagaContrato(Float.parseFloat(jTextField_edicionPaga.getText()));
                eve.obtenerNodo(indice).getDato().setNumeroPersonas(Integer.parseInt(jTextField_edicionPersonas.getText()));
                DefaultListModel dlm = new DefaultListModel();
                for (int i = 0; i < eve.size(); i++) {
                    try {
                        dlm.addElement(eve.obtenerNodo(i).toString());
                    } catch (Exception ex) {
                        Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                jList_Eventos.setModel(dlm);
                editarEvento.setVisible(false);
                verEventos.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButton_CONFIRMAR1ActionPerformed

