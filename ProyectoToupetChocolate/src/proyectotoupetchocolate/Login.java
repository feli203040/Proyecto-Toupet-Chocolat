    private void jButton_EnterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_EnterActionPerformed
        String userText = jTextField_userName.getText();
        String userPassword = jPasswordField_Pass.getText();
        ListaAdmin la = main.l0;
        ListaEmpleados lb = main.l1;
        if (userText.isEmpty() || userPassword.isEmpty()) {
            JOptionPane.showMessageDialog(null, "algun componente está vacio");
        } else {
            if (la.obtenerNodo2(userText) != null && userPassword.equals(la.obtenerNodo2(userText).getDato().getContrasenia())) {
                JOptionPane.showMessageDialog(null, "Bienvenido Admin");
                menuAdmin.setVisible(true);
                jPanel_back.setVisible(false);

            } else if (lb.obtenerNodo2(userText) != null && userPassword.equals(lb.obtenerNodo2(userText).getDato().getContrasenia())) {
                JOptionPane.showMessageDialog(null, "Bienvenido User");
                menuUser.setVisible(true);
                jPanel_back.setVisible(false);
            } else {
                JOptionPane.showMessageDialog(null, "Usuario / Contraseña Incorrectos");
            }
        }
    }//GEN-LAST:event_jButton_EnterActionPerformed

