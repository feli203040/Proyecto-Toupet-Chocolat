    private void jButton_CREARUSUARIOActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_CREARUSUARIOActionPerformed
        // TODO add your handling code here:
        if (jTextField_nombreRealUsuario.getText().isEmpty() || jTextField_apellidoUsuario.getText().isEmpty()
                || jTextField_numCedulaUser.getText().isEmpty() || jTextField_direccionUser.getText().isEmpty()
                || jTextField_nombreUsuario.getText().isEmpty() || jTextField_contrasenaUser.getText().isEmpty()) {

            JOptionPane.showMessageDialog(null, "Debe rellenar todos los campos.");

        } else {
            String nombre = jTextField_nombreRealUsuario.getText();
            String apellido = jTextField_apellidoUsuario.getText();
            String cedula = jTextField_numCedulaUser.getText();
            String direccion = jTextField_direccionUser.getText();
            String usuario = jTextField_nombreUsuario.getText();
            String contrasena = jTextField_contrasenaUser.getText();
            if (l1.obtenerNodo2(jTextField_nombreUsuario.getText()) != null) {
                JOptionPane.showMessageDialog(null, "El nombre de usuario ingresado esta en uso.");
            } else {
                l1.agregarInicio(new Empleado(nombre, apellido, cedula, direccion, usuario, contrasena, 0, 0, 0, 0, 0));
                DefaultListModel dlm = new DefaultListModel();
                ListaEmpleados lb = main.l1;
                for (int i = 0; i < lb.cantidad(); i++) {
                    try {
                        dlm.addElement(lb.obtenerNodo(i).toString());
                    } catch (Exception ex) {
                        Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                jList_usuarios.setModel(dlm);
                verUsuarios.setVisible(true);
                crearUsuario.setVisible(false);
            }
        }
    }//GEN-LAST:event_jButton_CREARUSUARIOActionPerformed

